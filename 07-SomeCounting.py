#the print statements displays the counting sequences required
print (" Count up 0 to 50 (+1)")

#the for loop iterates from 0 to 50 by 1
for count in range (0,51,1):
    
    #each count is printed on a new line
    print (count)

print ("Count down 50 to 0 (-1)")
for count in range (50,-1,-1):
    print (count)

print ("Count up 30 to 50 (+1)")
for count in range (30,51,1):
    print(count)

print ("Count down 50 to 10 (-2)")
for count in range (50,9,-2):
    print (count)
    
print ("Count up to 100 to 200 (+5)")
for count in range (100,201,5):
    print (count)

